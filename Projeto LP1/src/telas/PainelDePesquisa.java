    private void Atualiza_Arte() {
        DefaultTableModel model =(DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        for (Arte a : p.getCad()) {
            model.addRow(obj(a));
        }
    }
    
    
    private Object[] obj(Arte a){
        try {
            Object[] arte = {a.getTitulo(), a.getAno(), a.getNome(), a.getCategoria(), a.getTombo()} ;
            return arte;
        } catch (Exception e) {
        }
        return null;
    }
